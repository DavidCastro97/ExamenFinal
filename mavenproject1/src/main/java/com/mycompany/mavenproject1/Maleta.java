public class Maleta {
    private int Peso;
    private int ancho;
    private int Largo;
    private int Fondo;
    private int suma;

    public int getPeso() {
        return Peso;
    }

    public void setPeso(int Peso) {
        this.Peso = Peso;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }

    public int getLargo() {
        return Largo;
    }

    public void setLargo(int Largo) {
        this.Largo = Largo;
    }

    public int getFondo() {
        return Fondo;
    }

    public void setFondo(int Fondo) {
        this.Fondo = Fondo;
    }

    public int getSuma() {
        return suma;
    }

    public void setSuma(int suma) {
        this.suma = suma;
    }

    
}
